document.getElementById("vestido").addEventListener("click", function () {
    var subpagina = document.getElementById("subpagina");
    subpagina.style.display = "block";
});
document.getElementById("fecharSubpagina").addEventListener("click", function () {
    var subpagina = document.getElementById("subpagina");
    subpagina.style.display = "none";
});